import type { Node } from '@xyflow/react'
import type { PersistedWorkspaceState, TerminalNodeData } from '../types'

export function toRuntimeNodes(workspace: PersistedWorkspaceState): Node<TerminalNodeData>[] {
  return workspace.nodes.map(node => ({
    id: node.id,
    type: node.kind === 'task' ? 'taskNode' : 'terminalNode',
    position: node.position,
    data: {
      sessionId: '',
      title: node.title,
      width: node.width,
      height: node.height,
      kind: node.kind,
      status: node.status,
      startedAt: node.startedAt,
      endedAt: node.endedAt,
      exitCode: node.exitCode,
      lastError: node.lastError,
      agent: node.agent,
      task:
        node.kind === 'task'
          ? (node.task ?? {
              requirement: '',
              status: 'todo',
              linkedAgentNodeId: null,
              lastRunAt: null,
              autoGeneratedTitle: false,
            })
          : null,
    },
    draggable: true,
    selectable: true,
  }))
}
